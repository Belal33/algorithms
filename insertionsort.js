function insertionSort(arr) {
  let j
  for (let i = 1; i < arr.length; i++){
    let el = arr[i]
    for ( j= i - 1; j >=0; j--){
      if (arr[j]>el) {
        arr[j+1]=arr[j]
      } else {
        break
      }
    }
    arr[j + 1] = el
  }
  return(arr)
}

